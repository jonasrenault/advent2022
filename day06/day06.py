# Read puzzle input
with open("input.txt", "r") as f:
    puzzle = f.readlines()

# %%
from collections import Counter

puzzle = puzzle[0].strip()
marker = None
for i in range(4, len(puzzle)):
    if len(Counter(puzzle[i - 4 : i])) == 4:
        marker = i
        break
print(marker)

